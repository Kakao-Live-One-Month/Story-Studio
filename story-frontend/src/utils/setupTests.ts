// jest-dom adds custom jest matchers for asserting on DOM nodes.
// allows you to do things like:
// expect(element).toHaveTextContent(/react/i)
// learn more: https://github.com/testing-library/jest-dom
import '@testing-library/jest-dom';


// expect(element).toHaveTextContent(/react/i): 특정 DOM 요소가 "react"라는 텍스트를 포함하고 있는지 검사합니다. 이 검사는 대소문자를 구분하지 않습니다(/i 플래그 때문에).
// expect(element).toBeVisible(): 특정 DOM 요소가 시각적으로 보이는지 검사합니다.
// expect(element).toHaveClass('class-name'): 특정 DOM 요소가 주어진 클래스 이름을 가지고 있는지 검사합니다.
// @testing-library/jest-dom 라이브러리의 주요 목적은 Jest를 사용하는 테스트 케이스에서 DOM 요소들을 더 쉽고 직관적으로 검사할 수 있도록 하는 것입니다. 이는 테스트 코드의 가독성과 유지 보수성을 향상시키는 데 도움이 됩니다.


